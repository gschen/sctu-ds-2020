# 1、求给定数的阶乘
x=1
y=int(input("请输入一个数字:"))
for i in range(1,y+1):
    x=x*i
print(x)

# 2、单利公式为：单利=（P x T x R）/ 100其中，P是本金T是时间，R是利率
# 例如输入：P = 10000
#       R = 5
#       T = 5
# 输出：2500
# 要求:P、T、R都是input输入的，不能固定。
P=int(input('P='))
T=int(input('T='))
R=int(input('R='))
f=((P*T*R)/100)
print(f)

