# 4、求数组中的前n个数的平方和。
# 要求：n需要是input输入，且小于数组长度，不能固定。
l=[14,25,98,75,23,1,4,56,59]
a=0
n=int(input('n='))
l=l[:n]
for x in l:
    if n>0:
        a+=x*x
print(a)

# 5、交换列表中的任意两个元素：[14,25,98,75,23,1,4,56,59]
# 要求，被置换的两个位置需要input输入。
l=[14,25,98,75,23,1,4,56,59]
x=int(input('第一位:'))
y=int(input('第二位:'))
a=l[x]
b=l[y]
l[x]=b
l[y]=a
print(l)