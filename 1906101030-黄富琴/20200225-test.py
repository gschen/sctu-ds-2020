#集合
A={'a','b','c','c',1}
B=set('aabbcce')
print(A,B)
#集合并
print(A|B)
#集合差（补）
print(A-B)
print(B-A)
#集合交
print(A&B)
print(A^B)
#添加元素
A.add('a')
B.update({1,3},[4,2],'e')
#删除元素的三种方法
A.remove('a')
A.remove('f')
A.discard('f')

A.pop()
A.pop('a')
print(A)
print(B)

#字典
dic={'name':'张三','age':19,'school':'sctu'}
print(dic)
#修改数据
dic['name']='王五'
#查找数据
dic.get('name')
dic.get('address')
dic.setdefault('address','成都')
#增加数据
dic['class']='1班'
print(dic)
#删除数据
del dic['name']
dic.pop('age')
dic.popitem()
