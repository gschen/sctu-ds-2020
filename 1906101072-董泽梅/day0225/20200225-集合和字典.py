#集合
#构建集合两种方式
A={'a','b','c','c','1'}
B=set('aabbcce')
#集合之间的运算
#集合的差（补）
print(A-B)
#集合的并
print(A|B)
#交
print(A&B)
#不同时包含
print(A^B)
#集合的增加的两种方法
A.add('d')
B.update({1,3},[4,2],'e')
print(A)
print(B)
#删除元素的三种方法
A.remove('a')
#不存在时报错
A.remove('f')
#不存在时不报错
A.discard('f')
#随机删除
A.pop()
print(A.pop())

